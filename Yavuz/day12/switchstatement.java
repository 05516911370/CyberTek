package day12;

public class switchstatement {
    public static void main(String[] args) {

      int a = 10;

        switch (a){

            case 5:
                System.out.println("Five");
                break;

            case 1:
                System.out.println("One");
                break;


            default:
                System.out.println("Invalid case");
                break;










        }
















    }
}
